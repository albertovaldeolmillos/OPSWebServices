DECLARE
    nCount  NUMBER;

BEGIN

/* ################################################################################################################################################################# */
/* ### MAP_GUARDS ################################################################################################################################################## */
/* ################################################################################################################################################################# */

SELECT COUNT(*) INTO nCount FROM all_tab_columns WHERE (UPPER(table_name) = 'MAP_GUARDS') AND (UPPER(column_name) = 'GUA_STATUS');

IF (nCount = 0) THEN

    EXECUTE IMMEDIATE 'alter table MAP_GUARDS add GUA_STATUS number default 0 not null';

    EXECUTE IMMEDIATE 'comment on column MAP_GUARDS.GUA_STATUS is ''Estado (0 = Posición Inválida, 1 = Posición Válida Inactiva, 2 = Posición Válida Activa)''';

END IF;

/* ################################################################################################################################################################# */
/* ### FIN ######################################################################################################################################################### */
/* ################################################################################################################################################################# */

END;
